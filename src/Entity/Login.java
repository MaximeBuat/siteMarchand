package Entity;

/**
 *
 * @author maxim
 */
public class Login {
    private String login;
    private String mdp;
    
    public Login(String unLogin, String unMdp){
        login = unLogin;
        mdp = unMdp;
    }
    
    public Login(String unLogin){
        login = unLogin;
    }

    /**
     * @return the login
     */
    public String getLogin() {
        return login;
    }

    /**
     * @param login the login to set
     */
    public void setLogin(String login) {
        this.login = login;
    }

    /**
     * @return the mdp
     */
    public String getMdp() {
        return mdp;
    }

    /**
     * @param mdp the mdp to set
     */
    public void setMdp(String mdp) {
        this.mdp = mdp;
    }
    
}
